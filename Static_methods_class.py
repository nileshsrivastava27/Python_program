class NileshMaths:

    @staticmethod
    def add(x,y):
        print("Sum is",x+y)
    @staticmethod
    def multiply(x,y):
        print("multiplication",x*y)
    @staticmethod
    def average(x,y):
        print("the average is",(x+y)/2)

    
NileshMaths.add(5,9)
NileshMaths.multiply(8,19)
NileshMaths.average(100,200)